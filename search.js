
function searchBar(){
    var txtbar = document.getElementById("search");
    txtbar.style.visibility = "visible";
    txtbar.style.transition = "all 0.2s ease-out 0s";
}

function searchBarHidden(){
    var txtbar = document.getElementById("search");
    txtbar.style.visibility = "hidden";
    txtbar.style.transition = "all 0.2s ease-out 0s";
}